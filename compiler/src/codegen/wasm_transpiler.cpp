void WasmTranspiler::visit(IntegerLiteral* node) {
    emit("(f64.const " + std::to_string(static_cast<double>(node->value)) + ")");
}

void WasmTranspiler::visit(FloatLiteral* node) {
    emit("(f64.const " + std::to_string(node->value) + ")");
}

void WasmTranspiler::visit(StringLiteral* node) {
    // Allocate string literal and return pointer
    int offset = allocateString(node->value);
    emit("(i32.const " + std::to_string(offset) + ") ;; string: \"" + escapeStringForWat(node->value) + "\"");
}

void WasmTranspiler::visit(BooleanLiteral* node) {
    emit(node->value ? "(f64.const 1)" : "(f64.const 0)");
}

void WasmTranspiler::visit(NullLiteral* node) {
    (void)node;
    emit("(i32.const 0) ;; null pointer");
}

void WasmTranspiler::visit(Identifier* node) {
    // Check if this is a struct variable for type tracking
    if (variableTypes.count(node->name)) {
        emit("(local.get $" + node->name + ") ;; " + node->name + ": " + variableTypes[node->name]);
    } else {
        emit("(local.get $" + node->name + ")");
    }
}

void WasmTranspiler::visit(BinaryExpression* node) {
    std::string op = node->op;

    // Handle string concatenation
    if (op == "+" && dynamic_cast<StringLiteral*>(node->left.get())) {
        // String concatenation - for now, just return left pointer
        // TODO: Implement proper string concatenation with new allocation
        node->left->accept(*this);
        emit(" ;; string concat (TODO: proper implementation)");
        return;
    }

    // Emit operands first (stack-based)
    node->left->accept(*this);
    emit(" ");
    node->right->accept(*this);
    emit(" ");

    // Then emit the operation
    if (op == "+") emit("(f64.add)");
    else if (op == "-") emit("(f64.sub)");
    else if (op == "*") emit("(f64.mul)");
    else if (op == "/") emit("(f64.div)");
    else if (op == "%") {
        // Modulo: use rem instruction for f64
        // Stack: a b -> a % b
        // We need to be careful - save values first
        // Actually, we can use the stack directly:
        // a b f64.rem
        emit("(f64.rem)");
    }
    else if (op == "==" || op == "!=") {
        emit(op == "==" ? "(f64.eq)" : "(f64.ne)");
    }
    else if (op == "<") emit("(f64.lt)");
    else if (op == "<=") emit("(f64.le)");
    else if (op == ">") emit("(f64.gt)");
    else if (op == ">=") emit("(f64.ge)");
    else if (op == "&&" || op == "and") {
        // Convert f64 to i32 for logical ops
        // Left operand is f64, right is f64
        // (f64.ne (f64.const 0)) converts f64 to i32 bool
        emit("(f64.const 0) (f64.ne) ");  // left -> i32 bool
        emit("(f64.const 0) (f64.ne) ");  // right -> i32 bool
        emit("i32.and (if (result f64) (then (f64.const 1)) (else (f64.const 0)))");
    }
    else if (op == "||" || op == "or") {
        emit("(f64.const 0) (f64.ne) ");
        emit("(f64.const 0) (f64.ne) ");
        emit("i32.or (if (result f64) (then (f64.const 1)) (else (f64.const 0)))");
    }
    else {
        emit(";; unsupported op: " + op);
    }
}

void WasmTranspiler::visit(UnaryExpression* node) {
    std::string op = node->op;
    node->operand->accept(*this);

    if (op == "-") {
        emit(" (f64.neg)");
    } else if (op == "!" || op == "not") {
        emit(" (f64.const 0) (f64.eq) (if (result f64) (then (f64.const 1)) (else (f64.const 0)))");
    }
}

void WasmTranspiler::visit(AssignmentExpression* node) {
    // Get the variable name from left side
    if (auto* id = dynamic_cast<Identifier*>(node->left.get())) {
        node->right->accept(*this);
        emit(" (local.set $" + id->name + ")");
    } else if (auto* memberExpr = dynamic_cast<MemberExpression*>(node->left.get())) {
        // Handle member assignment: obj.field = value
        // For now, simplified - proper implementation needs to track struct offsets
        node->right->accept(*this);
        emit(" ;; member assignment (struct field)");
    }
}

void WasmTranspiler::visit(CallExpression* node) {
    // Handle print specially for strings
    if (node->callee == "print" && !node->arguments.empty()) {
        // Check if first argument is a string literal
        if (auto* strLit = dynamic_cast<StringLiteral*>(node->arguments[0].get())) {
            int offset = allocateString(strLit->value);
            emit("(i32.const " + std::to_string(offset) + ")");
            emit(" (i32.const " + std::to_string(strLit->value.length()) + ")");
            emit(" (call $print_string)");
            return;
        }
    }

    // Emit arguments first
    for (const auto& arg : node->arguments) {
        arg->accept(*this);
        emit(" ");
    }

    // Handle built-in functions
    if (node->callee == "print") {
        emit("(call $print_f64)");
    } else if (node->callee == "len" || node->callee == "length") {
        // Length function - assumes i32 pointer to array/string
        emit("(i32.load) ;; get length from pointer");
    } else if (node->callee == "alloc") {
        emit("(call $alloc)");
    } else {
        emit("(call $" + node->callee + ")");
    }
}

void WasmTranspiler::visit(ArrayLiteral* node) {
    // Allocate array in linear memory
    // Format: [length: i32][element_size: i32][element0: f64][element1: f64]...
    int numElements = static_cast<int>(node->elements.size());
    int arraySize = 8 + (numElements * 8);  // header (8 bytes) + elements (8 bytes each)

    emit("\n");
    indent();
    emit(";; Array literal with " + std::to_string(numElements) + " elements\n");

    // Allocate memory for array
    indent();
    emit("(call $alloc (i32.const " + std::to_string(arraySize) + "))\n");

    // Store the array pointer in a temp local
    emit("(local.set $__arr_ptr)\n");
    indent();
    emit("(local.get $__arr_ptr)\n");

    // Store length
    indent();
    emit("i32.const " + std::to_string(numElements) + "\n");
    indent();
    emit("i32.store\n");

    // Store element size (8 for f64)
    indent();
    emit("(local.get $__arr_ptr)\n");
    indent();
    emit("i32.const 4\n");
    indent();
    emit("i32.add\n");  // offset to element_size field
    indent();
    emit("i32.const 8\n");  // element size = 8 bytes (f64)
    indent();
    emit("i32.store\n");

    // Store each element
    for (int i = 0; i < numElements; i++) {
        indent();
        emit("(local.get $__arr_ptr)\n");
        indent();
        emit("i32.const " + std::to_string(8 + i * 8) + "\n");  // offset to element
        indent();
        emit("i32.add\n");
        node->elements[i]->accept(*this);
        emit("\n");
        indent();
        emit("f64.store\n");
    }

    // Return array pointer
    indent();
    emit("(local.get $__arr_ptr)");
}

void WasmTranspiler::visit(ArrayIndexExpression* node) {
    // Get array pointer and index
    indent();
    node->array->accept(*this);
    emit("\n");
    indent();
    node->index->accept(*this);
    emit("\n");

    // Calculate address: arr_ptr + 8 + index * 8
    indent();
    emit(";; Array index access\n");
    indent();
    emit("(local.set $__arr_idx)\n");  // save index
    indent();
    emit("(local.set $__arr_ptr)\n");  // save array pointer

    indent();
    emit("(local.get $__arr_ptr)\n");
    indent();
    emit("i32.const 8\n");
    indent();
    emit("i32.add\n");  // skip header
    indent();
    emit("(local.get $__arr_idx)\n");
    indent();
    emit("i32.const 8\n");
    indent();
    emit("i32.mul\n");  // index * 8 (element size)
    indent();
    emit("i32.add\n");
    indent();
    emit("f64.load");
}

void WasmTranspiler::visit(ArrayAssignmentExpression* node) {
    // Get array pointer, index, and value
    indent();
    node->array->accept(*this);
    emit("\n");
    indent();
    node->index->accept(*this);
    emit("\n");
    indent();
    node->value->accept(*this);
    emit("\n");

    indent();
    emit(";; Array assignment\n");
    indent();
    emit("(local.set $__arr_val)\n");  // save value
    indent();
    emit("(local.set $__arr_idx)\n");  // save index
    indent();
    emit("(local.set $__arr_ptr)\n");  // save array pointer

    // Calculate address and store
    indent();
    emit("(local.get $__arr_ptr)\n");
    indent();
    emit("i32.const 8\n");
    indent();
    emit("i32.add\n");  // skip header
    indent();
    emit("(local.get $__arr_idx)\n");
    indent();
    emit("i32.const 8\n");
    indent();
    emit("i32.mul\n");  // index * 8
    indent();
    emit("i32.add\n");
    indent();
    emit("(local.get $__arr_val)\n");
    indent();
    emit("f64.store");
}

