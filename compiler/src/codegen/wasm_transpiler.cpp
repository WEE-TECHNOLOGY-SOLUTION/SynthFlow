void WasmTranspiler::indent() {
    for (int i = 0; i < indentLevel; ++i) {
        output << "  ";
    }
}

void WasmTranspiler::emit(const std::string& code) {
    output << code;
}

void WasmTranspiler::emitLine(const std::string& code) {
    indent();
    output << code << "\n";
}

int WasmTranspiler::getLocalIndex(const std::string& name) {
    auto it = localVariables.find(name);
    if (it != localVariables.end()) {
        return it->second;
    }
    int index = nextLocalIndex++;
    localVariables[name] = index;
    return index;
}

int WasmTranspiler::alignUp(int offset, int alignment) {
    return ((offset + alignment - 1) / alignment) * alignment;
}

int WasmTranspiler::allocateString(const std::string& str) {
    // Align to 4-byte boundary
    int offset = alignUp(stringDataOffset, 4);
    stringLiterals.push_back({offset, str});
    // 4 bytes for length + string bytes + null terminator
    stringDataOffset = offset + 4 + static_cast<int>(str.length());
    return offset;
}

int WasmTranspiler::allocateHeapBlock(int size) {
    // Align size to 8-byte boundary
    size = alignUp(size, ALIGNMENT);
    int ptr = heapPointer;
    heapPointer += size;
    return ptr;
}

std::string WasmTranspiler::escapeStringForWat(const std::string& str) {
    std::string result;
    for (char c : str) {
        switch (c) {
            case '"': result += "\\\""; break;
            case '\\': result += "\\\\"; break;
            case '\n': result += "\\n"; break;
            case '\r': result += "\\r"; break;
            case '\t': result += "\\t"; break;
            default:
                if (c >= 32 && c < 127) {
                    result += c;
                } else {
                    std::ostringstream oss;
                    oss << "\\" << std::setfill('0') << std::setw(2) << std::hex << static_cast<int>(static_cast<unsigned char>(c));
                    result += oss.str();
                }
        }
    }
    return result;
}

std::string WasmTranspiler::stringToWatHex(const std::string& str) {
    std::string result;
    for (unsigned char c : str) {
        std::ostringstream oss;
        oss << std::setfill('0') << std::setw(2) << std::hex << static_cast<int>(c);
        result += "\\" + oss.str();
    }
    return result;
}

