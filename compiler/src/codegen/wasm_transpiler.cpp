void WasmTranspiler::visit(MatchExpression* node) {
    emit(";; match expression - converting to if/else chain\n");
    indent();

    // Get the subject value
    node->subject->accept(*this);
    emit(" (local.set $__match_val)\n");

    // Generate if/else chain for each case
    bool first = true;
    for (const auto& matchCase : node->cases) {
        if (!matchCase.pattern) {
            // Default case
            indent();
            emit("(block $__match_end\n");
            indentLevel++;

            // If all previous cases failed, execute default
            indent();
            matchCase.result->accept(*this);

            indentLevel--;
            indent();
            emit(")\n");
        } else {
            indent();
            if (!first) {
                emit("(else\n");
                indentLevel++;
            }

            // Compare subject with pattern
            emit("(local.get $__match_val) ");
            matchCase.pattern->accept(*this);
            emit(" (f64.eq)\n");

            indent();
            emit("(if\n");
            indentLevel++;
            indent();
            emit("(then\n");
            indentLevel++;

            // Execute the result expression
            indent();
            matchCase.result->accept(*this);
            emit("\n");

            indentLevel--;
            indent();
            emit(")\n");

            first = false;
        }
    }

    // Close all the if/else blocks
    for (size_t i = 0; i < node->cases.size(); i++) {
        if (node->cases[i].pattern) {
            indentLevel--;
            indent();
            emit(")\n");
        }
    }
}

void WasmTranspiler::visit(CompoundAssignment* node) {
    if (auto* id = dynamic_cast<Identifier*>(node->target.get())) {
        emit("(local.get $" + id->name + ") ");
        node->value->accept(*this);

        if (node->op == "+=") emit(" (f64.add)");
        else if (node->op == "-=") emit(" (f64.sub)");
        else if (node->op == "*=") emit(" (f64.mul)");
        else if (node->op == "/=") emit(" (f64.div)");

        emit(" (local.set $" + id->name + ")");
    }
}

void WasmTranspiler::visit(UpdateExpression* node) {
    if (auto* id = dynamic_cast<Identifier*>(node->operand.get())) {
        emit("(local.get $" + id->name + ") (f64.const 1) ");
        emit(node->op == "++" ? "(f64.add)" : "(f64.sub)");
        emit(" (local.set $" + id->name + ")");
    }
}

