void WasmTranspiler::visit(MapLiteral* node) {
    // Allocate map in linear memory
    // Format: [count: i32][capacity: i32][entries...]
    // Each entry: [key_ptr: i32][key_len: i32][value: f64]
    int numEntries = static_cast<int>(node->entries.size());
    // Map header: count (4) + capacity (4) = 8 bytes
    // Each entry: key_ptr (4) + key_len (4) + value (8) = 16 bytes
    int mapSize = 8 + (numEntries * 16);

    emit("\n");
    indent();
    emit(";; Map literal with " + std::to_string(numEntries) + " entries\n");

    // Allocate memory for map
    indent();
    emit("(call $alloc (i32.const " + std::to_string(mapSize) + "))\n");
    emit("(local.set $__map_ptr)\n");

    // Store count
    indent();
    emit("(local.get $__map_ptr)\n");
    indent();
    emit("i32.const " + std::to_string(numEntries) + "\n");
    indent();
    emit("i32.store\n");

    // Store capacity
    indent();
    emit("(local.get $__map_ptr)\n");
    indent();
    emit("i32.const 4\n");
    indent();
    emit("i32.add\n");
    indent();
    emit("i32.const " + std::to_string(numEntries) + "\n");
    indent();
    emit("i32.store\n");

    // Store each entry
    for (int i = 0; i < numEntries; i++) {
        const auto& entry = node->entries[i];
        int entryOffset = 8 + i * 16;

        // Store key pointer (first evaluate key - should be string)
        indent();
        emit("(local.get $__map_ptr)\n");
        indent();
        emit("i32.const " + std::to_string(entryOffset) + "\n");
        indent();
        emit("i32.add\n");
        entry.first->accept(*this);
        emit("\n");
        indent();
        emit("i32.store\n");

        // Store value
        indent();
        emit("(local.get $__map_ptr)\n");
        indent();
        emit("i32.const " + std::to_string(entryOffset + 8) + "\n");
        indent();
        emit("i32.add\n");
        entry.second->accept(*this);
        emit("\n");
        indent();
        emit("f64.store\n");
    }

    // Return map pointer
    indent();
    emit("(local.get $__map_ptr)");
}

void WasmTranspiler::visit(MemberExpression* node) {
    // Check if object is an identifier and we know its type
    if (auto* id = dynamic_cast<Identifier*>(node->object.get())) {
        std::string varName = id->name;
        std::string varType = variableTypes.count(varName) ? variableTypes[varName] : "";

        // Check if it's a struct we know about
        if (structFields.count(varType)) {
            const auto& fields = structFields[varType];
            int fieldOffset = 0;

            // Find field offset
            for (const auto& field : fields) {
                if (field.first == node->member) {
                    // Found the field
                    indent();
                    emit(";; Accessing " + varType + "." + node->member + " at offset " + std::to_string(fieldOffset) + "\n");
                    indent();
                    emit("(local.get $" + varName + ")\n");
                    indent();
                    emit("i32.const " + std::to_string(fieldOffset) + "\n");
                    indent();
                    emit("i32.add\n");
                    indent();
                    emit("f64.load");
                    return;
                }
                fieldOffset += 8;  // Each field is 8 bytes
            }
        }
    }

    // Fallback: try array.length for arrays
    if (node->member == "length" || node->member == "len") {
        indent();
        node->object->accept(*this);
        emit("\n");
        indent();
        emit("i32.load ;; get array/map length");
        return;
    }

    // Generic member access - just load from offset based on member name hash
    indent();
    emit(";; Member expression: ." + node->member + "\n");
    indent();
    node->object->accept(*this);
    emit("\n");
    indent();
    emit(";; TODO: proper field offset lookup\n");
    indent();
    emit("f64.load");
}

void WasmTranspiler::visit(SelfExpression* node) {
    (void)node;
    emit("(local.get $self) ;; self reference");
}

void WasmTranspiler::visit(MethodCallExpression* node) {
    // Method call: obj.method(args)
    indent();
    emit(";; Method call: " + node->method + "\n");

    // Emit arguments first
    for (const auto& arg : node->arguments) {
        arg->accept(*this);
        emit(" ");
    }

    // Check if this is a known struct method
    if (auto* id = dynamic_cast<Identifier*>(node->object.get())) {
        std::string varName = id->name;
        std::string varType = variableTypes.count(varName) ? variableTypes[varName] : "";

        if (structFields.count(varType)) {
            // This is a struct method call
            emit("(call $" + varType + "_" + node->method + ")");
            return;
        }
    }

    // Generic method call - use naming convention struct_method
    indent();
    node->object->accept(*this);
    emit("\n");
    indent();
    emit("(call $" + node->method + ") ;; generic method call");
}

// ========================================
// Statement Visitors
// ========================================

void WasmTranspiler::visit(VariableDeclaration* node) {
    int idx = getLocalIndex(node->name);

    // Track type for struct variables
    if (!node->typeName.empty()) {
        variableTypes[node->name] = node->typeName;
    }

    indent();

    // Determine the type of the variable
    bool isPointerType = false;
    if (node->typeName == "string" || node->typeName == "String") {
        isPointerType = true;
    } else if (structFields.count(node->typeName)) {
        isPointerType = true;
    } else if (node->initializer) {
        // Check if initializer is a string, array, or map literal
        if (dynamic_cast<StringLiteral*>(node->initializer.get()) ||
            dynamic_cast<ArrayLiteral*>(node->initializer.get()) ||
            dynamic_cast<MapLiteral*>(node->initializer.get())) {
            isPointerType = true;
        }
    }

    if (isPointerType) {
        emit("(local $" + node->name + " i32)");  // Pointer type
    } else {
        emit("(local $" + node->name + " f64)");  // Numeric type
    }

    if (node->initializer) {
        emit(" ");
        node->initializer->accept(*this);
        emit(" (local.set $" + node->name + ")");
    }
    emit("\n");
}

void WasmTranspiler::visit(ExpressionStatement* node) {
    indent();
    node->expression->accept(*this);
    emit("\n");
}

void WasmTranspiler::visit(BlockStatement* node) {
    emitLine("(block");
    indentLevel++;
    for (const auto& stmt : node->statements) {
        stmt->accept(*this);
    }
    indentLevel--;
    emitLine(")");
}

void WasmTranspiler::visit(IfStatement* node) {
    indent();
    node->condition->accept(*this);
    emit("\n");
    emitLine("(if (result f64) (then");
    indentLevel++;
    node->thenBranch->accept(*this);
    indentLevel--;

    if (node->elseBranch) {
        emitLine(") (else");
        indentLevel++;
        node->elseBranch->accept(*this);
        indentLevel--;
        emitLine("))");
    } else {
        emitLine("))");
    }
}

void WasmTranspiler::visit(WhileStatement* node) {
    emitLine("(block $break (result f64)");
    indentLevel++;
    emitLine("(loop $continue");
    indentLevel++;

    // Condition check
    indent();
    node->condition->accept(*this);
    emit(" (i32.trunc_f64_s) (i32.eqz) (br_if $break)\n");

    // Loop body
    node->body->accept(*this);

    emitLine("(br $continue)");
    indentLevel--;
    emitLine(")");
    indentLevel--;
    emitLine(")");
}

void WasmTranspiler::visit(ForStatement* node) {
    // Convert for loop to while loop structure
    emitLine("(block $break (result f64)");
    indentLevel++;

    // Initializer
    if (node->initializer) {
        node->initializer->accept(*this);
    }

    emitLine("(loop $continue");
    indentLevel++;

    // Condition
    if (node->condition) {
        indent();
        node->condition->accept(*this);
        emit(" (i32.trunc_f64_s) (i32.eqz) (br_if $break)\n");
    }

    // Body
    node->body->accept(*this);

    // Increment
    if (node->increment) {
        indent();
        node->increment->accept(*this);
        emit("\n");
    }

    emitLine("(br $continue)");
    indentLevel--;
    emitLine(")");
    indentLevel--;
    emitLine(")");
}

void WasmTranspiler::visit(BreakStatement*) {
    emitLine("(f64.const 0) (br $break)");
}

void WasmTranspiler::visit(ContinueStatement*) {
    emitLine("(br $continue)");
}

void WasmTranspiler::visit(FunctionDeclaration* node) {
    // Reset local variable tracking for new function
    localVariables.clear();
    nextLocalIndex = 0;
    inFunction = true;

    indent();
    emit("(func $" + node->name + " (export \"" + node->name + "\")");

    // Parameters
    for (const auto& param : node->parameters) {
        emit(" (param $" + param + " f64)");
        getLocalIndex(param);
    }

    // Result type (assume f64 for all functions with return)
    emit(" (result f64)");
    emit("\n");

    indentLevel++;

    // Add temp locals for array/map operations
    emitLine("(local $__arr_ptr i32)  ;; temp for array operations");
    emitLine("(local $__arr_idx i32)  ;; temp for array index");
    emitLine("(local $__arr_val f64)  ;; temp for array value");
    emitLine("(local $__map_ptr i32)  ;; temp for map operations");
    emitLine("(local $__match_val f64)  ;; temp for match expression");

    // Function body
    node->body->accept(*this);

    // Default return if not explicit
    emitLine("(f64.const 0)");

    indentLevel--;
    emitLine(")");
    emitLine("");

    inFunction = false;
}

void WasmTranspiler::visit(ReturnStatement* node) {
    indent();
    if (node->value) {
        node->value->accept(*this);
        emit(" ");
    } else {
        emit("(f64.const 0) ");
    }
    emit("(return)\n");
}

void WasmTranspiler::visit(TryStatement* node) {
    // WebAssembly doesn't have native try/catch (in MVP)
    // We emit the try block and add a comment
    emitLine(";; try/catch - executing try block only (Wasm MVP limitation)");
    if (node->tryBlock) {
        node->tryBlock->accept(*this);
    }
}

void WasmTranspiler::visit(ImportStatement* node) {
    // Imports are handled at module level
    emitLine(";; import: " + node->moduleName + (node->alias.empty() ? "" : " as " + node->alias));
}

void WasmTranspiler::visit(StructDeclaration* node) {
    // Struct declarations are processed in the first pass
    // Here we just emit a comment
    int structSize = structSizes.count(node->name) ? structSizes[node->name] : 0;

    emitLine(";; struct " + node->name + " (size: " + std::to_string(structSize) + " bytes)");

    // Emit struct layout comment
    int offset = 0;
    for (const auto& field : node->fields) {
        emitLine(";;   " + field.name + ": " + field.typeName + " @ offset " + std::to_string(offset));
        offset += 8;
    }

    // Emit constructor function
    emitLine("(func $new_" + node->name + " (export \"new_" + node->name + "\") (result i32)");
    indentLevel++;

    // Allocate struct
    emitLine("(local $self i32)");
    emitLine("(call $alloc (i32.const " + std::to_string(structSize) + "))");
    emitLine("(local.set $self)");

    // Initialize fields with defaults
    offset = 0;
    for (const auto& field : node->fields) {
        if (field.defaultValue) {
            emitLine("(local.get $self)");
            emitLine("i32.const " + std::to_string(offset));
            emitLine("i32.add");
            field.defaultValue->accept(*this);
            emit(" f64.store\n");
        } else {
            // Zero-initialize
            emitLine("(local.get $self)");
            emitLine("i32.const " + std::to_string(offset));
            emitLine("i32.add");
            emitLine("(f64.const 0) f64.store");
        }
        offset += 8;
    }

    emitLine("(local.get $self)");
    indentLevel--;
    emitLine(")");

    // Emit methods
    for (const auto& method : node->methods) {
        // Reset local variable tracking
        localVariables.clear();
        nextLocalIndex = 0;

        indent();
        emit("(func $" + node->name + "_" + method->name + " (export \"" + node->name + "." + method->name + "\")");

        // Add self parameter
        emit(" (param $self i32)");

        // Parameters
        for (const auto& param : method->parameters) {
            emit(" (param $" + param + " f64)");
            getLocalIndex(param);
        }

        emit(" (result f64)\n");
        indentLevel++;

        // Add temp locals
        emitLine("(local $__arr_ptr i32)");
        emitLine("(local $__arr_idx i32)");
        emitLine("(local $__arr_val f64)");
        emitLine("(local $__map_ptr i32)");
        emitLine("(local $__match_val f64)");

        // Method body
        method->body->accept(*this);

        emitLine("(f64.const 0)");

        indentLevel--;
        emitLine(")");
        emitLine("");
    }
}